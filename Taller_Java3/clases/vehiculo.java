package clases;

public class vehiculo {
    private String placa, marca, telefono, nombre;

    public vehiculo(String placa, String marca, String telefono, String nombre) {
        this.placa = placa;
        this.marca = marca;
        this.telefono = telefono;
        this.nombre = nombre;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
